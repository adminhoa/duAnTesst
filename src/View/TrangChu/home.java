    public  void filldata(){
        tbl_model.setRowCount(0);
        for (KhachHangTrangHangViewModel kh : listKhachHang) {
            tbl_model.addRow(new Object[]{
                kh.getTen(),kh.isGioiTinh() == true?"Nam":"Nữ",kh.getSoDienThoai(),kh.getSoluong()
            });
        }
    }
